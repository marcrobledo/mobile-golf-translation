bank_140_get_string:
	push	bc
	push	de
	push	hl
	ld		hl, _STRING_POINTERS8C
	sla		e
	rl		d
	add		hl, de
	;here hl=pointer offset
	ld		e, [hl]
	inc		hl
	ld		d, [hl]
	;here de=string offset - 
	ld		hl, _STRINGS8C
	add		hl, de
