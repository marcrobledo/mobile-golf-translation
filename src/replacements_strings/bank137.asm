bank_137_copy_string_to_d880:
	push	af
	ld		a, $01
	call	bank_137_get_string
	pop		af
	ret

bank_137_get_string:
	push	bc
	push	de
	push	hl
	ld		hl, _STRING_POINTERS89
	sla		e
	rl		d
	add		hl, de
	;here hl=pointer offset
	ld		e, [hl]
	inc		hl
	ld		d, [hl]
	;here de=string offset - 
	ld		hl, _STRINGS89
	add		hl, de
	or		a
	jr		nz, .set_de_d880
	ld		de, $c600
	jr		.keep_copying
.set_de_d880:
	ld		de, $d880
.keep_copying:
	ld		a, [hli]
	ld		[de], a
	inc		de
	or		a
	jr		nz, .keep_copying
	
	pop		hl
	pop		de
	pop		bc
	ret
